      <h3>Tips for working from Home</h3>

      <p>Following the events of this year a large number of us have been forced to work remotely to help mitigate the spread of COVID-19. Thid transition has been a challenge for many people that value
      routine and face to face communication with work collegues. However, the following tips can help make the home an effective workplace that is good for productivity as well as morale.
</p>


<h3>Tip #1: Try to keep your usual morning routine.</h3>
<p>Keeping a consistant morning routine will set you up for the day ahead. Wake up at your usual time, get washed and get dressed and think of ways that you can effectively use the time you would 
have spent if you were commuting by car or public transport. Perhaps you could drink your morning coffee on a walk, cook a healthy breakfast, go for a cycle or do a home workout. These activities will help 
your body to release feel-good hormones to get you motivated. 
</p>


<h3>Tip #2: Stay Connected with Your Work Colleagues</h3>
<p>Working from home for days on end can feel a little isolating. It’s important to stay in touch with your colleagues to stay connected. Schedule a team call for 9.30am each morning to catch up, discuss the 
day’s work and share your thoughts. Having some structure each day will keep you motivated and engaged in your work. Following the meeting, create a to do list for each day and make it your goal to complete it. 
</p>


<h3>Tip #3: Take Regular Breaks</h3>
<p>Just as you would at the office, take regular breaks to make coffee, have a snack or even put on a wash. Have virtual coffee breaks via video call with your colleagues where you can chat about things other
 than work for a few minutes. Breaks are important for your mental health and will help with your work too. 
</p>



<h3>Tip #4: Take Your Lunch Break as Normal</h3>
<p>Having your lunch at home means you can cook delicious, healthy lunches and save some money that would have been spent in expensive cafes. Try to eat balanced meals that include protein, fat and 
carbohydrates to stay energised. You should eat your lunch away from your work space and make sure that you are fully logged out of your computer. It’s also hugely important to get outside for some air on your lunch break.
Whether it's for a walk with the dog, a jog or run, or even a trip to the supermarket for supplies, getting out in the fresh air is crucial for your mental health and job performance. 
</p>



<h3>Tip #5: Stick to Normal Working Hours</h3>
<p>Sticking to a time schedule can be difficult when working from home. However, having set times and tasks will give your day structure and will help to keep you stimulated throughout the day. Avoid waking up late, taking
 longer lunches or putting off tasks that will need to be completed later in the day spilling over into your free time. 
</p>


<h3>Tip #6: Avoid Checking Your Phone and Social Media Accounts</h3>
<p>We know, the temptation is much stronger when you’re working from home but try to avoid being on your personal phone to check your notifications or messages. Switch your phone on to “do not disturb” mode and only check
 it when you are taking a break.

</p>



<h3>Tip #7: Make Plans for When Work is Finished</h3>
<p>Now is the time to cook new things, discover new music, watch movie classics, dive into a good book, declutter your home or complete a fitness challenge such as Couch to 5K. Try to include at least one of these activities 
for your evenings. 

</p>

<h3>Tip #8: Finish Work on Time</h3>
<p>Begin wrapping up your work in preparation to finish at your usual closing time. Once that time has come the evening is yours to enjoy. Switch off the computer and switch off your mind. Move from your work space into your 
sitting room and give yourself a few moments to adjust. 



</p>
