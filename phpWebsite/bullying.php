      <p>
        Cyberbullying is a new form of bullying. This form of bullying happens a lot on social media, online forums, text and email.

        Examples of cyberbullying include:




      </p>

      <ul>
        <li><p>abusive messages or slagging on Facebook, Twitter, Snapchat and Instagram</p></li>
        <li><p>offensive comments on videos or posts</p></li>
        <li><p>spreading rumours online</p></li>
        <li><p>hacking into your online accounts</p></li>
        <li><p>posting offensive images</P></li>

      </ul>

      <p>Cyberbullying can happen to anyone. People are able to create fake or anonymous profiles and hurtful comments. These people would usually not say these things in real life. You can also be bullied by someone you know.

        Online bullying is serious and many new laws have been introduced to protect people.</p>

        <h1>How to avoid cyberbullying</h1>

        <p>Never give out your passwords
Always keep your passwords and PIN numbers to yourself. If you use a public computer, log out of your email and social media accounts. Use good passwords for your phone.<br><br>

Pick your social friends carefully
Whatever you post online can be seen by everyone who has access to your page or the discussion board. Make sure you are okay with sharing the information. Even if you have a private account, anything you post is considered public.<br><br>

Use netiquette
Be polite to other people online. Think about what you're saying and whether it might be hurtful or embarrass them in public, even if it's funny.<br><br>

Don't send a message when you are angry
Wait until you have calmed down and had time to think.<br><br>

Remember: The internet doesn't forget
Posts and messages can never be permanently deleted. Snapchat offers temporary posts. These posts can easily be screen-grabbed.<br><br>

How to deal with cyberbullying
Don't reply
Even though you might really want to, don't rise to the bait and reply to messages from someone who's bullying you. They want to know that they've got you worried and upset. Chances are if you never reply they'll get bored and leave you alone.<br><br>

Report or block someone
You can block people from phoning or sending texts. You can also report them. Find out how to report or block people in the help section or frequently asked questions section (FAQ) of a website or app.<br><br>

Go offline
If you feel like social media is becoming too much, switch off. Consider your time spent on social media and whether you need to keep your account.<br><br>

Inform your phone company or Internet Service Provider (ISP)
They can block texts, calls or online messages from specific people.<br><br>

Change your contact details
Get a new username, a new email address, a new mobile number and only give them to your closest friends.<br><br>

Tell someone
If it's bothering you, don't keep it to yourself. Talk to someone about it.<br><br>

Inform the Gardaí
If the messages are ever threatening or it's getting serious, tell the Gardaí. It's against the law to threaten people, and the Gardaí can put a stop to it. They are there to keep you safe, and they generally want to know about stuff like this.<br><br>

Keep a record
You don't have to read the messages, but keep them and keep a record of the time and date. This can act as evidence if you ever need it. It can also help the Gardaí or your ISP find out where the messages are coming from.</p>

